double distanceBetween(double lat1, double lon1, double lat2, double lon2, char unit);
double deg2rad(double);
double rad2deg(double);

bool isCarReachDestination(double &directionInfo, double &distanceInfo, double reachDistance, double sourceLon, double sourceLat, double destinationLon, double destinationLat){
	double x, y;
	
	x = destinationLon - sourceLon;
	y = destinationLat - sourceLat;

	directionInfo = atan(x / y) * 180 / M_PI;

	if (y < 0) {
		directionInfo += 180;
	}else if (x < 0) {
		directionInfo += 360;
	}

	distanceInfo = distanceBetween(sourceLat, sourceLon, destinationLat, destinationLon, 'K');
	if (reachDistance > distanceInfo) {
		return true;
	}
	return false;
}

double distanceBetween(double lat1, double lon1, double lat2, double lon2, char unit) {
	double theta, dist;
	theta = lon1 - lon2;
	dist = sin(deg2rad(lat1)) * sin(deg2rad(lat2)) + cos(deg2rad(lat1)) * cos(deg2rad(lat2)) * cos(deg2rad(theta));
	dist = acos(dist);
	dist = rad2deg(dist);
	dist = dist * 60 * 1.1515;
	switch (unit) {
	case 'M':
		break;
	case 'K':
		dist = dist * 1.609344;
		break;
	case 'N':
		dist = dist * 0.8684;
		break;
	}
	return (dist);
}

/*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
/*::  This function converts decimal degrees to radians             :*/
/*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
double deg2rad(double deg) {
	return (deg * M_PI / 180);
}

/*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
/*::  This function converts radians to decimal degrees             :*/
/*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
double rad2deg(double rad) {
	return (rad * 180 / M_PI);
}
